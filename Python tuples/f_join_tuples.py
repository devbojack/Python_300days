# ____________________________________________________________________Python - Join Tuples
# To join two or more tuples you can use the + operator:
tuple1 = ('a', 'b', 'c')
tuple2 = (1, 2, 3)
tuple3 = tuple1 + tuple2
print(tuple3)

# ____________________________________________________________________Python - Multiply tuples
# If you want to multiply the content of a tuple a given number of times, you can use the * operator:

print("____________________________________________________________________Python - Multiply tuples")
fruits = ("apple", "mango", "banana")
multuple = fruits * 2
print(multuple)